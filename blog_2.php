        //blog tabs
            //selecting elements
            const tabs = document.querySelector(".section-blog__wrapper");
            const tabBtns = document.querySelectorAll(".section-blog__btn");
            const tabContent = document.querySelectorAll(".section-blog__box");

            //listening to events on the blog
            tabs.addEventListener("click", function (e) {
            const id = e.target.dataset.key;
            if (id) {
                tabBtns.forEach((btn) => {
                    btn.classList.remove("active");
                });

                e.target.classList.add("active");

                tabContent.forEach((content) => {
                    content.classList.remove("active");
                });

                const elem = document.getElementById(id);
                elem.classList.add("active");
            }
            });
